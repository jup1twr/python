#crie uma função chamada SOMA que receba
#dois numers como parametros e retorne a soma deles.
#depois peça ao user digite dois numeros, utilize a funcao e devolva o resultado.

def soma():
    n1 = float(input("Digite um número: "))
    n2 = float(input("Digite outro número: "))
